package com.young.pact.domain.commongoods;

import java.io.Serializable;
import java.util.List;

/**
 * @描述 : 合同物业交接物品
 * @author : GuoXiaoPeng
 * @创建时间 : 2018年8月2日 下午4:50:40
 */
public class GoodsVO implements Serializable{

    /**
    * @Fields serialVersionUID : TODO( )
    */
    private static final long serialVersionUID = 1L;
    /** @Fields id:唯一标识**/
    private Long id;
    /** @Fields pactCode:合同编码**/
    private String pactCode;
    /** @Fields pactType:合同类型**/
    private String pactType;
    /** @Fields goodsName:物品名称**/
    private String goodsName;
    /** @Fields unit:单位**/
    private String unit;
    /** @Fields number:数量**/
    private Integer number;
    /** @Fields goodsStatus:物品状况**/
    private String goodsStatus;
    /** @Fields remark:备注**/
    private String remark;
    /** @Fields remark:物品照片(集合)**/
    private List<CommonGoodsPicVO> commonGoodsPicList;
    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public String getGoodsName() {
        return goodsName;
    }
    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }
    public String getUnit() {
        return unit;
    }
    public void setUnit(String unit) {
        this.unit = unit;
    }
    public Integer getNumber() {
        return number;
    }
    public void setNumber(Integer number) {
        this.number = number;
    }
    public String getGoodsStatus() {
        return goodsStatus;
    }
    public void setGoodsStatus(String goodsStatus) {
        this.goodsStatus = goodsStatus;
    }
    public String getRemark() {
        return remark;
    }
    public void setRemark(String remark) {
        this.remark = remark;
    }
    public List<CommonGoodsPicVO> getCommonGoodsPicList() {
        return commonGoodsPicList;
    }
    public void setCommonGoodsPicList(List<CommonGoodsPicVO> commonGoodsPicList) {
        this.commonGoodsPicList = commonGoodsPicList;
    }
    public String getPactCode() {
        return pactCode;
    }
    public void setPactCode(String pactCode) {
        this.pactCode = pactCode;
    }
    public String getPactType() {
        return pactType;
    }
    public void setPactType(String pactType) {
        this.pactType = pactType;
    }
}

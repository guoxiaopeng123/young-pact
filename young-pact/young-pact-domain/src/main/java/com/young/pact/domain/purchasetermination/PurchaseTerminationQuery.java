package com.young.pact.domain.purchasetermination;

import java.util.List;

import com.young.common.domain.BaseDomain;

public class PurchaseTerminationQuery extends BaseDomain {

    private static final long serialVersionUID = 1L;
    /*** @Fields keyWords:关键字*/
    private String keyWords;
    /*** @Fields pactEndDateMin:原合同截止时间（最小值）*/
    private String pactEndDateMin;
    /*** @Fields pactEndDateMax:原合同截止时间（最大值）*/
    private String pactEndDateMax;
    /*** @Fields terminationDateMin:解约时间（最小值）*/
    private String terminationDateMin;
    /*** @Fields terminationDateMax:解约时间（最大值）*/
    private String terminationDateMax;
    /*** @Fields deptCode:部门编码*/
    private String deptCode;
    /*** @Fields type:解约类型*/
    private String type;
    /*** @Fields state:审核状态*/
    private Integer state;
    /*** @Fields scope:数据作用域*/
    private Integer scope;
    /*** @Fields userPin:登录人PIN*/
    private String userPin;
    /*** @Fields pinList:本人及下属集合*/
    private List<String> pinList;
    /*** @Fields deptList:本部门及下属部门集合*/
    private List<String> deptList;
    /*** @Fields terminationCode:解约协议编码*/
    private String terminationCode;
    public PurchaseTerminationQuery(){}

    public String getKeyWords() {
        return keyWords;
    }

    public void setKeyWords(String keyWords) {
        this.keyWords = keyWords;
    }

    public String getPactEndDateMin() {
        return pactEndDateMin;
    }

    public void setPactEndDateMin(String pactEndDateMin) {
        this.pactEndDateMin = pactEndDateMin;
    }

    public String getPactEndDateMax() {
        return pactEndDateMax;
    }

    public void setPactEndDateMax(String pactEndDateMax) {
        this.pactEndDateMax = pactEndDateMax;
    }

    public String getTerminationDateMin() {
        return terminationDateMin;
    }

    public void setTerminationDateMin(String terminationDateMin) {
        this.terminationDateMin = terminationDateMin;
    }

    public String getTerminationDateMax() {
        return terminationDateMax;
    }

    public void setTerminationDateMax(String terminationDateMax) {
        this.terminationDateMax = terminationDateMax;
    }

    public String getDeptCode() {
        return deptCode;
    }

    public void setDeptCode(String deptCode) {
        this.deptCode = deptCode;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Integer getState() {
        return state;
    }

    public void setState(Integer state) {
        this.state = state;
    }


    public String getUserPin() {
        return userPin;
    }

    public void setUserPin(String userPin) {
        this.userPin = userPin;
    }

    public List<String> getPinList() {
        return pinList;
    }

    public void setPinList(List<String> pinList) {
        this.pinList = pinList;
    }

    public List<String> getDeptList() {
        return deptList;
    }

    public void setDeptList(List<String> deptList) {
        this.deptList = deptList;
    }

    public Integer getScope() {
        return scope;
    }

    public void setScope(Integer scope) {
        this.scope = scope;
    }

    public String getTerminationCode() {
        return terminationCode;
    }

    public void setTerminationCode(String terminationCode) {
        this.terminationCode = terminationCode;
    }
}
